// The "Guide" node spliced at the START of a package group:
// the package's prose. With extra prose pages (only plgg
// core today) it nests them beneath the Overview; otherwise
// it is a plain link to the single prose page.
const guideNode = (group: PackageGroup): Node => {
  const docs = group.docs ?? [];
  return {
    text: "Guide",
    link: group.overview,
    items: docs,
  };
};

// The "API reference" node spliced at the END of a package
// group: a link to the package's API landing, with one leaf
// per category when the package's reference spans more than
// one. Returns `[]` when generation has not run or the
// package opts out, so the group degrades to prose-only.
const apiReferenceNode = (
  group: PackageGroup,
): ReadonlyArray<Node> => {
  if (group.api === false) {
    return [];
  }
  const entry = generatedApi[group.key];
  if (!entry) {
    return [];
  }
  return [
    {
      text: "API reference",
      link: entry.link,
      items: entry.items.map((item) =>
        leaf(item.text, item.link),
      ),
    },
  ];
};

// Assemble one package's top-level sidebar group: the Guide
// (prose) then the API reference, the two reading paths side
// by side.
const packageGroup = (
  group: PackageGroup,
): {
  text: string;
  items: ReadonlyArray<Node>;
} => ({
  text: group.text,
  items: [
    guideNode(group),
    ...apiReferenceNode(group),
  ],
});


// The exhaustive, per-package API reference is
// auto-generated by `scripts/gen-api.mjs`, which writes
// `api/typedoc-sidebar.json` as a per-package map: each
// package name maps to `{ link, items }` (its landing link
// plus one leaf per category). Load it when present so each
// package group gets its API reference spliced in; with no
// generation it falls back to an empty map, leaving the
// package groups prose-only.
const guideRoot = dirname(
  fileURLToPath(import.meta.url),
);
const apiSidebarFile = join(
  guideRoot,
  "api",
  "typedoc-sidebar.json",
);

// One manifest entry per package: `{ link, items }` where
// each `items` leaf is a `{ text, link }` category page.
type ApiEntry = {
  link: string;
  items: ReadonlyArray<{
    text: string;
    link: string;
  }>;
};
type ApiManifest = Record<string, ApiEntry>;

const isApiEntry = (
  value: unknown,
): value is ApiEntry =>
  typeof value === "object" &&
  value !== null &&
  "link" in value &&
  "items" in value;

// Read and shape the generated manifest into a typed
// `ApiManifest` without escape hatches: parse to `unknown`,
// then keep only the well-formed `{ link, items }` entries.
const loadApiManifest = (): ApiManifest => {
  if (!existsSync(apiSidebarFile)) {
    return {};
  }
  const parsed: unknown = JSON.parse(
    readFileSync(apiSidebarFile, "utf8"),
  );
  if (
    typeof parsed !== "object" ||
    parsed === null
  ) {
    return {};
  }
  const out: Record<string, ApiEntry> = {};
  for (const [key, value] of Object.entries(
    parsed,
  )) {
    if (isApiEntry(value)) {
      out[key] = {
        link: value.link,
        items: value.items.map((item) => ({
          text: item.text,
          link: item.link,
        })),
      };
    }
  }
  return out;
};

const generatedApi = loadApiManifest();

// A sidebar node as authored here: every node carries an
// explicit `items` array (the leaf case is `[]`) so it
// validates against plgg-press's `SidebarItem`, whose
// `items` is required and whose `link` is optional.
type Node = {
  text: string;
  link?: string;
  items: ReadonlyArray<Node>;
};

const leaf = (
  text: string,
  link: string,
): Node => ({ text, link, items: [] });

